            <p>The Bainbridge High School Sailing Team fosters growth in the areas of sailing skills, responsibility, respect, and character.  See "Sailor Attributes" document. These attributes and the ability to work together to achieve a common goal are important life skills.  We operate under the philosophy that every Team member is important to the Team's success and an asset to the Team in its efforts to achieve excellence.  The most important sailor is always "the Team."</p>
            <p>Currently, in compliance with Washington Interscholastic Activities Association and Bainbridge High School rules, the Team practices in the spring sports season, because two of the three High School (Interscholastic Sailing Association) national championships are held in the spring.<p>
            <p>If a Team member is interested in the third national championship single-handed sailing as opposed to the double-handed events of the spring  then practice in the fall is allowed, as the District eliminations and national championship are held in the fall.  Many Team members choose to sail in the Bainbridge Island Metro Park and Recreation District open fall sailing program in order to keep their skills sharp.  The BIMPRD program is not to be confused with Team practice.</p>
            <p>During the spring competition season, regular attendance at all Team practices is required of all Team members. See link to Practices</p>
            
            <p><a href="http://bhssailing.net/the-coaches/">Coaches:</a></p>
            <ul>
              <li>Make all decisions as to team composition, eligibility, membership, and participation; and ensure conformance with BHS eligibility rules.</li>
              <li>Set regatta schedule.</li>
              <li>Arrange pre-season workouts.</li>
              <li>Schedule and run practices.</li>
              <li>Coordinate between Team and support organizations.</li>
              <li>Arrange for boat and equipment maintenance.</li>
            </ul>
            <p><a href="http://www.biparks.org/index.htm">Bainbridge Island Metro Park & Recreation District (BIMPRD):</a></p>
            <ul>
              <li>Owns all sailboats.</li>
              <li>Provides shoreside and upland storage for all sailboats.</li>
              <li>Owns, stores, and maintains all support equipment (sailboat trailers, coaching/safety powerboats).</li>
              <li>Provides training venues (Eagle Harbor dock and "bock").</li>
              <li>Provides insurance.</li>
              <li>Provides volunteer coach(es), and screens coaches and volunteer assistants.</li>
              <li>Permits use of BIMPRD vehicles for Team travel in accordance with their rules and availability.</li>
            </ul>
            <p><a href="http://www.bisd303.org/Domain/8">Bainbridge High School:</a></p>
            <ul>
              <li>Allows the Team to operate as a non-funded high school club sport.</li>
              <li>Allows the sailors to earn high school sports "letters."</li>
              <li>Requires the Team to follow all BHS athletic eligibility rules.</li>
            </ul>
            <p><a href="http://bhssailing.net/friends-of-bihss/">Friends of Bainbridge Island High School Sailing (FBIHSS):</a></p>
            <ul>
              <li>Operates as an all-volunteer, non-profit organization providing the sole source of logistical and financial support to the Bainbridge High School Sailing Team.</li>
              <li>Acquires funds to support the operational expenses of regatta participation, travel, and training, including sailboat and equipment maintenance.</li>
              <li>Raises funds to help cover the costs of replacement of the two fleets of competition sailboats.</li>
            </ul>
