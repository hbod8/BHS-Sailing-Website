            <p>Friends of Bainbridge Island High School Sailing (FBIHSS) is organized exclusively for charitable and educational purposes to provide financial and logistical support to the Bainbridge High School sailing program.  As a high school club sport, the Team receives no funding from the Bainbridge Island School District, so Friends of BIHSS support allows this independent, self-funded Team to maintain racing sailboats and associated equipment in a competitive and safe condition, and permits Team members to travel to regional and national regattas under the umbrella of the <a href="http://nwisa.hssailing.org/">North West Interscholastic Sailing Association (NWISA)</a> and <a href="http://hssailing.org/">Interscholastic Sailing Association (ISSA)</a>.</p>
            <p>Our organization is operated on an all-volunteer, not-for-profit basis.  Parent and community volunteers perform all of the logistical, administrative, boat repair, and equipment maintenance tasks.  100% of this volunteer effort is applied in support of Bainbridge High School sailing.</p>
            <p>Expenses associated with the Sailing Team's competition (for example, travel costs, regatta fees, and boat maintenance costs) are covered partly through participation fees paid by each Team member and partly through funds raised by the sailors and by volunteers of the organization.</p>
            <p>Through a mutually beneficial arrangement with the Bainbridge Island Metro Park and Recreation District, Friends of BIHSS helps fund the acquisition of the racing sailboats, and maintains the boats during the competition season through the volunteered labor of organization members, coaches, the sailors, and community members, and through funding the costs of maintenance and repair materials and parts.  In return, the Park District arranges for Team coaching, holds title to the sailboats, and provides sailing venues, shoreside facilities, dry storage facilities, coaching and safety powerboats, insurance, and some transportation services.</p>
            <p>The approximately 14 sailboats comprising the competition fleets are on a periodic replacement schedule of six years, consistent with the top college and high school teams in the country.  This replacement interval helps to ensure that the Team always has access to the most competitive and updated training and competition facilities reasonably available.</p>
            <p>Friends of Bainbridge Island High School Sailing is registered as a Section 501(c)(3) public charity.  Your tax deductible donations help BHS Sailors in a variety of ways, including purchasing competition sailboats, maintaining equipment, funding scholarships, and offsetting the cost of High School sailor participation in regional and national regattas.  Please see the <a href="http://bhssailing.net/how-you-can-help/">How You Can Help</a> and Ongoing Sailboat Replacement Campaigns (http://bhssailing.net/ongoing-sailboat-replacement-campaigns/  ) pages if you would like to contribute financially to the Friends of Bainbridge Island High School Sailing in support of the BHS Sailing Team.</p>
