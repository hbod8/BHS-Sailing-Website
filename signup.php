              <li>Driver/Chaperone Guidelines are found here:  <a href="http://bhssailing.net/wp-content/uploads/2016/02/FBIHSS-Driver-Chaperone-Policy.pdf">FBIHSS Driver-Chaperone Policy</a> .</li>
