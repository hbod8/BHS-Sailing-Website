            <p>There are several important aspects to participating on the Bainbridge High School Sailing Team you should know before you sign up.</p>
            <h3>What's required of the sailor:</h3>
          <ul>
            <li>Participate on the team with an open mind, exhibit good <a href="http://bhssailing.net/wp-content/uploads/2013/01/Sportsmanship.pdf">sportsmanship</a>, and support other team members and coaches.</li>
            <li>Attend practices and let the coaches know when you will not be there.</li>
            <li>Participate in the regattas.</li>
            <li>Assist with the logistical tasks of the team.</li>
            <li>Participate in Friends of BIHSS fundraiser events in support of the Team.</li>
          </ul>
            <h3>What's required of the parent(s):</h3>
            <ul>
              <li>Participate with the Friends of <a href="http://bhssailing.net/friends-of-bihss/">Bainbridge Island High School Sailing</a> to help out with BHS Sailing Team activities, including driving to/from team events, and assisting with other Team activities.</li>
              <li>AssistFriends of Bainbridge Island High School Sailing with fundraising.</li>
            </ul>
            <p>Safety is our top concern and precautions are taken to keep everyone safe. That being said, sailing can be dangerous since it involves activity on the water. All sailors are expected to wear PFDs (personal flotation devices) when in and around the water and follow the direction provided by coaches and regatta staff. There is always a danger of concussion (click <a href="http://bhssailing.net/wp-content/uploads/2013/01/Concussion.pdf">here</a> for more information) along with cuts and bruises.</p>
            <h3>Cost:</h3>
            <p>The participation fee for BHS Sailing Team is set annually. The season fee, which covers regatta fees, transportation, and related team logistics and administration costs, for the 2016 spring season is $430.</p>
            <h3>Forms:</h3>
            <h3>For Sailors:</h3>
            <p>Release of Liability:  Sign and submit the Friends of Bainbridge Island High School Sailing Release of Liability and Hold Harmless Agreement, which is found here:  <a href="http://bhssailing.net/wp-content/uploads/2016/02/FBIHSS-Sailor-Liability-Waiver.pdf">FBIHSS Sailor Liability Waiver</a> .</p>
            <p>Bainbridge Athletic Club:  Prior to participating in pre-season fitness and strength workouts at the Bainbridge Athletic Club, prepare and submit the following BAC Waiver form:  <a href="http://bhssailing.net/wp-content/uploads/2014/02/BAC.waiver.pdf">BAC.waiver</a> .</p>
            <p>Bainbridge High School Sports Registration:  Please complete the student sport registration process at the following BISD website:  http://www.bisd303.org/Page/8981 .  You will need to establish a "FamilyID" account in order to complete registration, by following the instructions on this webpage.  Click on the button for "Spring 20XX Athletic Registration" and then on the button for  "Sailing" registration and complete the form.  There is no BISD fee to register for sailing as a club sport.  Physical examinations:  Please note the following process on the webpage for physical exams, as this entails an additional step:  "PHYSICAL EXAMS: A physical exam must be current within the past 24 months and signed by a physician. If the student's physical expires during the season, they will not be able to participate until a new physical exam is received.  Please bring your new or updated physical to the main office."  You must complete the full registration process before the first team practice.  The BISD will provide the Coach with a list of all sailors who are fully registered. <a href="http://www.bisd303.org/Page/8981">BISD Athletics/Forms may be found here</a> .</p>
